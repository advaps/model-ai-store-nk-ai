// Enhanced function to get the most appropriate image for a model
export const getModelImage = (name, category) => {
  const lowerName = name.toLowerCase();
  
  // Check for specific keyword matches first
  for (const [keyword, imageUrl] of Object.entries(modelImages)) {
    if (lowerName.includes(keyword.replace('_', ' '))) {
      return imageUrl;
    }
  }
  
  // Fallback to category-based defaults
  if (category === "Indoor") {
    return modelImages.default_indoor;
  } else if (category === "Outdoor") {
    return modelImages.default_outdoor;
  }
  
  return modelImages.default_fallback;
};
